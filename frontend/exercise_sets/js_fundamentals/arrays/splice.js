// A custom implementation of the `Array.prototype.splice` method.

function splice(array, start, deleteCount, ...args) {
  const beforeStartArray     = array.slice(0, start);

  const afterDeleteIndex     = (start + deleteCount);
  const deletedElementsArray = array.slice(start, afterDeleteIndex)
  const afterDeleteArray     = array.slice(afterDeleteIndex);

  array.length = 0;
  array.push(...beforeStartArray,
             ...args,
             ...afterDeleteArray
  )

  return deletedElementsArray;
}

console.log(splice([1, 2, 3], 1, 2));              // [2, 3]
console.log(splice([1, 2, 3], 1, 3));              // [2, 3]
console.log(splice([1, 2, 3], 1, 0));              // []
console.log(splice([1, 2, 3], 0, 1));              // [1]
console.log(splice([1, 2, 3], 1, 0, 'a'));         // []

const arr2 = [1, 2, 3];
console.log(splice(arr2, 1, 1, 'two'));             // [2]
console.log(arr2);                                  // [1, "two", 3]

const arr3 = [1, 2, 3];
console.log(splice(arr3, 1, 2, 'two', 'three'));    // [2, 3]
console.log(arr3);                                  // [1, "two", "three"]

const arr4 = [1, 2, 3];
console.log(splice(arr4, 1, 0));                    // []
console.log(splice(arr4, 1, 0, 'a'));               // []
console.log(arr4);                                  // [1, "a", 2, 3]

const arr5 = [1, 2, 3];
console.log(splice(arr5, 0, 0, 'a'));               // []
console.log(arr5);                                  // ["a", 1, 2, 3]
